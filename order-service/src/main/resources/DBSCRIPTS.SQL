CREATE TABLE ORDER_DETAIL(
    ORDERID int NOT NULL AUTO_INCREMENT,
    ORDER_NAME VARCHAR(20),
    ITEM VARCHAR(20),
    QUANTITY INT,
    AMOUNT DOUBLE,
    PAYMENT_METHOD VARCHAR(20),
    ADDRESS VARCHAR(20),
    STATUS VARCHAR(20),
    CREATED_DATE timestamp NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATED_DATE timestamp NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT ORDER_DETAILS_ORDERID_PK PRIMARY KEY(ORDERID)
);

CREATE TABLE PAYMENT(
    PAYMENT_ID int NOT NULL AUTO_INCREMENT,
    MODE VARCHAR(20),
    ORDER_ID int,
    AMOUNT INT,
    STATUS VARCHAR(20),
    CREATED_DATE timestamp NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATED_DATE timestamp NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT PAYMENT_ID_PK PRIMARY KEY(PAYMENT_ID)
);

CREATE TABLE WAREHOUSE(
    WAREHOUSE_ID int NOT NULL AUTO_INCREMENT,
    ITEM VARCHAR(20),
    QUANTITY int,
    CREATED_DATE timestamp NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATED_DATE timestamp NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT WAREHOUSE_ID_PK PRIMARY KEY(WAREHOUSE_ID)
);

CREATE TABLE DELIVERY(
    DELIVERY_ID int NOT NULL AUTO_INCREMENT,
    ORDER_ID int,
    ADDRESS VARCHAR(20),
    STATUS VARCHAR(20),
    CREATED_DATE timestamp NULL DEFAULT CURRENT_TIMESTAMP,
    UPDATED_DATE timestamp NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT DELIVERY_ID_PK PRIMARY KEY(DELIVERY_ID)
);